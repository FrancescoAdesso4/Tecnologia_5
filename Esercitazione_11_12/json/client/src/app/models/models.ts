export class AutoSportive{
    public nome:string;   
    public alimentazione: string;
    public potenza : string;
    public prezzo: string;
    public immagine: string;
    
    constructor(nome:string, alimentazione:string, potenza:string,  prezzo:string, immagine: string){
        this.nome = nome;   
        this.alimentazione = alimentazione;
        this.potenza = potenza;
        this.prezzo = prezzo;
        this.immagine = immagine;
    }
}

